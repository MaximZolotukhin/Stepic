"""
Подвиг 2. Вводится слово. Необходимо первую букву этого слова сделать заглавной, а остальные - малыми.
Результат отобразить на экране.

Sample Input: HELLO
Sample Output: Hello
"""
# Сохраняю данные в переменную
word = input()
# Метод capitalize() - делает первую букву этого слова заглавной
print(word.capitalize())
